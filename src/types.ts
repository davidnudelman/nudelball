/* ===== Full Game State ===== */

/**
 * The master game state object — the single source of truth for
 * all mutable game data. Serialised to localStorage for saves.
 */
export interface GameState {
  /** Manager name entered at game start */
  manager: string;
  /** ID of the human player's team (null before selection) */
  playerTeamId: number | null;
  /** Current season number (1-based) */
  season: number;
  /** Current week within the season (1-based, up to TOTAL_SEASON_WEEKS) */
  week: number;
  /** All teams in the league */
  teams: Team[];
  /** League fixtures grouped by division and round */
  fixtures: FixturesByDivision;

  /* --- Waiting / Return Pools (promotion/relegation rotation) --- */
  /** Teams waiting to enter the league (from WAITING_TEAMS_DATA) */
  waitingPool: WaitingTeam[];
  /** Teams that were relegated out, waiting to return after 2 seasons */
  returnPool: Array<WaitingTeam & { returnIn?: number }>;

  /* --- UI State --- */
  /** Whether squad list is sorted by position (true) or overall (false) */
  sortByPos: boolean;
  /** Whether an animated match is currently in progress */
  matchInProgress: boolean;
  /** Currently selected division tab on the Table view */
  tableDivTab: number;
  /** Currently selected division tab on the Calendar view */
  calDivTab: number;
  /** Currently selected division tab on the Scorers view (0 = all) */
  scorersDivTab: number;

  /* --- Scoring --- */
  /** Season top scorers dictionary */
  topScorers: TopScorers;

  /* --- History --- */
  /** Completed season summaries */
  seasonHistory: SeasonHistory[];

  /* --- Transfer Market --- */
  /** Available free agents for signing */
  freeAgents: FreeAgent[];
  /** Team budgets — key is team ID */
  budgets: Record<number, number>;
  /** Whether the transfer window is currently open */
  transferWindow: boolean;
  /** Whether the week-1 transfer reminder has been shown */
  transferReminderShown?: boolean;
  /** Log of transfers made this season */
  transferLog: TransferLogEntry[];
  /** Whether the one-time free first signing has been used */
  usedFreeSign?: boolean;

  /* --- Trophy Room --- */
  /** All-time and season records for Trophy Room */
  records: Records | null;

  /* --- Formation --- */
  /** Index into FORMATIONS for the human player's auto-pick formation */
  selectedFormationIdx: number;

  /* --- Tactics & Training (v2.0) --- */
  /** Currently selected match tactic */
  tactic: TacticId;
  /** Currently selected training focus */
  trainingFocus: TrainingFocus;

  /* --- Animated Match State (transient, not persisted meaningfully) --- */
  /** Number of substitutions used in the current animated match */
  matchSubs: number;
  /** Indices of players sent off by red card in the current animated match */
  matchRedCards: number[];

  /* --- Player Profile --- */
  /** Identifies the player profile currently being viewed */
  viewingPlayerId: ViewingPlayerId | null;

  /* --- v3.0 Features --- */

  /** Stadium facilities (#7) */
  facilities?: Facilities;
  /** Active sponsorship deal (#6) */
  sponsorship?: Sponsorship | null;
  /** Players on loan (#8) */
  loans?: LoanPlayer[];
  /** Youth prospects available for promotion (#14) */
  youthProspects?: YouthProspect[];
  /** Squad auto-rotation rules (#19) */
  squadRules?: SquadRules;
  /** Scout network level (#18): 0=basic, 1=all divs, 2=hidden gems */
  scoutLevel?: ScoutLevel;
  /** Active negotiation state (#17) */
  negotiation?: NegotiationState | null;

  /* --- Season Recap Gate (v3.1) --- */

  /** Pending season recap data — shown in dashboard before new season can start */
  seasonRecapData?: SeasonRecapData | null;
}

