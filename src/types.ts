/**
 * types.ts — Core TypeScript interfaces for the Nudelball game.
 *
 * All game objects, state, and data structures are defined here
 * so that every module imports from a single source of truth.
 */

/* ===== Primitive / Enum-Like Types ===== */

/** Valid player positions */
export type Position = 'GK' | 'DEF' | 'MID' | 'STR';

/** CSS class suffixes for position badge styling */
export type PositionCSS = 'gk' | 'def' | 'mid' | 'str';

/** Tactic identifiers */
export type TacticId = 'attack' | 'balanced' | 'defensive' | 'counter';

/** Training focus options (simplified: 3 focuses instead of 5) */
export type TrainingFocus = 'balanced' | 'fitness' | 'development';

/** Difficulty levels — easy gives AI handicap and budget boost, hard does the opposite */
export type Difficulty = 'easy' | 'normal' | 'hard';

/** Team strength display modes in settings */
export type StrengthDisplay = 'none' | 'power' | 'avg';

/** Theme modes */
export type Theme = 'dark' | 'light';

/** Supported languages */
export type Language = 'en' | 'pt' | 'es';

/** Transfer log entry types */
export type TransferType = 'buy' | 'sell';

/** Player role specializations (#5) */
export type PlayerRole = 'playmaker' | 'targetMan' | 'ballWinner' | 'speedster' | 'creator' | 'anchor' | null;

/** Manager reputation tiers (#12) */
export type ReputationTier = 'novice' | 'amateur' | 'semiPro' | 'professional' | 'worldClass' | 'legend';

/** Trophy types awarded at season end */
export type TrophyType = 'gold_trophy' | 'silver_trophy' | 'gold_medal' | 'silver_medal';

/** Match event types (used in both quick-sim and animated matches) */
export type MatchEventType = 'goal' | 'yellow' | 'red';

/** Sort mode for squad view */
export type SortMode = 'position' | 'overall';

/* ===== Player ===== */

/** A single player in a team's roster */
export interface Player {
  /** Full name (e.g. "Carlos García") */
  name: string;
  /** Natural position */
  pos: Position;
  /** Base skill rating (1-50) */
  skill: number;
  /** Current stamina percentage (0-100) */
  stamina: number;
  /** Player age in years */
  age: number;
  /** Matches remaining on injury (0 = healthy) */
  injuredFor: number;
  /** Currently assigned tactical position (null = unassigned) */
  assignedPos: Position | null;
  /** Whether this player is selected in the starting 11 */
  selected: boolean;
  /** Consecutive matches spent on the bench (3+ triggers "fresh" bonus) */
  benchStreak: number;
  /** Lifetime goals scored across all seasons */
  careerGoals: number;
  /** Lifetime appearances across all seasons */
  careerApps: number;
  /** Goals scored in the current season */
  seasonGoals: number;
  /** Appearances in the current season */
  seasonApps: number;
  /** Yellow cards received this season */
  seasonYellows: number;
  /** Red cards received this season */
  seasonReds: number;
  /**
   * Current form rating (-3 to +3).
   * Each point adjusts effective OVR by FORM_OVR_PCT (5%).
   */
  form: number;
  /** Consecutive results contributing to current form streak */
  formStreak: number;
  /** Matches remaining on suspension from cards (0 = available) */
  suspendedFor: number;
  /** Whether this player was generated by the youth academy (legacy, always false in v2.1+) */
  isAcademy: boolean;
  /** Player role specialization (#5) — gives bonuses when paired with matching tactics */
  role?: PlayerRole;
  /**
   * Flag set during animated match when a sub enters the pitch.
   * Subs receive a temporary +10% effective rating boost.
   * Cleared after each match — not persisted.
   */
  subbedIn?: boolean;
  /** Whether the player is "On Fire" — form +3 and 3+ recent goals */
  onFire?: boolean;
}

/* ===== Team ===== */

/** Season or cumulative stats for a team */
export interface Stats {
  /** Matches played */
  p: number;
  /** Wins */
  w: number;
  /** Draws */
  d: number;
  /** Losses */
  l: number;
  /** Goals for (scored) */
  gf: number;
  /** Goals against (conceded) */
  ga: number;
  /** Total points (3 per win, 1 per draw) */
  pts: number;
}

/** A trophy/medal earned at the end of a season */
export interface Trophy {
  /** Type of award */
  type: TrophyType;
  /** Season number when the trophy was earned */
  season: number;
}

/** A team in the active league */
export interface Team {
  /** Unique team index (0-based, matches position in G.teams array) */
  id: number;
  /** Display name (e.g. "Barcelona") */
  name: string;
  /** Current division (1-4) */
  div: number;
  /** Primary colour (hex string, e.g. "#ad1519") — used as flag/plate background candidate */
  c1: string;
  /** Secondary colour (hex string) — used as flag/plate text candidate */
  c2: string;
  /** ISO-ish country code for name generation (e.g. "ES", "EN", "BR") */
  country: string;
  /** Full squad roster */
  players: Player[];
  /** Cumulative all-time stats */
  stats: Stats;
  /** Current season stats (reset each new season) */
  seasonStats: Stats;
  /** Collection of trophies/medals earned */
  trophies: Trophy[];
  /** Index into FORMATIONS array — AI's chosen formation (randomised each season) */
  aiFormation: number;
  /** Team morale (-10 to +10) — affects team strength by ±5% (#4) */
  morale?: number;
  /** Rival team IDs for derby matches (#13) */
  rivals?: number[];
}

/** Raw team data used to initialise teams at game start (from TEAMS_DATA constant) */
export interface TeamData {
  name: string;
  div: number;
  c1: string;
  c2: string;
  country: string;
}

/** A team waiting in the pool to enter/re-enter the league (from WAITING_TEAMS_DATA) */
export interface WaitingTeam {
  name: string;
  c1: string;
  c2: string;
  country: string;
}

/* ===== Fixtures & Match Events ===== */

/** A single match event (goal, yellow card, or red card) */
export interface MatchEvent {
  /** Event type */
  type: MatchEventType;
  /** Team that the event belongs to */
  teamId: number;
  /** Minute of the match (1-90) */
  minute: number;
  /** Name of the player who scored (only for 'goal' events) */
  scorer?: string;
  /** Name of the player who received a card (only for 'yellow' / 'red' events) */
  playerName?: string;
  /** Narrative description of the event (generated by match engine) */
  narrative?: string;
}

/** An animated match event — uses 'min' instead of 'minute' */
export interface AnimatedMatchEvent {
  /** Event type */
  type: MatchEventType;
  /** Team that the event belongs to */
  teamId: number;
  /** Minute of the match (1-90) */
  min: number;
  /** Name of the player who scored (only for 'goal' events) */
  scorer?: string;
  /** Name of the player who received a card (only for 'yellow' / 'red' events) */
  playerName?: string;
}

/** Injury record generated after a match */
export interface MatchInjury {
  /** Name of the injured player */
  name: string;
  /** Team ID the player belongs to */
  teamId: number;
  /** Number of matches the player will miss */
  duration: number;
}

/** A single league fixture */
export interface Fixture {
  /** Home team ID */
  home: number;
  /** Away team ID */
  away: number;
  /** Home team goals (null if not yet played) */
  homeGoals: number | null;
  /** Away team goals (null if not yet played) */
  awayGoals: number | null;
  /** Match events (goals, cards) */
  events: MatchEvent[];
  /** Injuries generated after the match (added post-sim) */
  injuries?: MatchInjury[];
  /** Man of the Match player name (set after simulation) */
  motm?: string;
  /** Man of the Match team ID */
  motmTeamId?: number;
}

/**
 * Fixtures are stored by division number, where each division
 * contains an array of rounds (weeks), and each round contains
 * an array of fixtures.
 */
export type FixturesByDivision = Record<number, Fixture[][]>;

