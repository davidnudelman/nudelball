export interface NegotiationState {
  /** Seller team ID */
  sellerTeamId: number;
  /** Player index in seller's roster */
  playerIdx: number;
  /** Current asking price */
  askingPrice: number;
  /** Minimum price the seller will accept */
  minimumPrice: number;
  /** Rounds of negotiation remaining */
  roundsLeft: number;
}

