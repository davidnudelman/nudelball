/**
 * Look up a translation key in the current language, falling back to English.
 *
 * Supports variable interpolation via `{key}` placeholders.  Pass a plain
 * object as the second argument to substitute values, e.g.:
 *
 * ```ts
 * t(settings, 'playWeek', { week: 5 })  // "Play Week 5"
 * ```
 *
 * @param settings - The current settings object (provides `settings.lang`).
 * @param key      - The translation key to look up.
 * @param vars     - Optional key/value pairs to interpolate into the string.
 * @returns The translated (and interpolated) string, or the raw key if not found.
 */
export function t(
  settings: Settings,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const dict = LANG[settings.lang] || LANG.en;
  let str = dict[key];
  if (str === undefined) {
    str = LANG.en[key] || key;
  }
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), String(v));
    }
  }
  return str;
}
